public class RelationalOPEx{
      public static void main(String args[]){
        int num1=60;
        int num2=30;
        System.out.println(num1);
        System.out.println(num1==num2);
        System.out.println(num1>=num2);
        System.out.println(num1<=num2);
        System.out.println(num1!=num2);
        System.out.println(num1%=num2);

   }
} 
        